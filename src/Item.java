public class Item
{
	public double value;
	public double volume;
	public double weight;
	public double cost;

	public Item(double volume, double weight, double cost, double value)
	{
		this.value = value;
		this.volume = volume;
		this.weight = weight;
		this.cost = cost;
	}
}
